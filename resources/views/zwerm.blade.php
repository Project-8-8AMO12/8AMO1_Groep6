        <h2>Kenmerken voor een bijenzwerm;</h2>
        <p>Een bijenzwerm is een grote “bal of tros” met duizenden (tot wel 20.000) honingbijen die aan een voorwerp, bijvoorbeeld een tak, dakgoot of fiets, hangt. Imkers kunnen een bijenzwerm “scheppen” met behulp van een korf of bak. Het is belangrijk dat wij er bij kunnen, eventueel met een ladder (graag zelf voor zorgen, kunnen wij niet meenemen). Als een zwerm onbereikbaar is, dan kunnen we niet “scheppen”.
            Soms kruipt een zwerm in spouwmuren, schoorstenen en tussen de muren of aftimmering. Dan kunnen we niet bij de zwerm en kunnen we helaas niets doen</p>
